package com.takeaway.model;

/*
菜品对象
 */
public class Dishes {
    //菜品名称
    private String name;
    //菜品价格
    private double price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
